package com.coolweather.app.model;

public class Space {
	
	private int id;
	private String Name;
	private String Code;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
 		this.id = id;
	}
	
	public String getName() {
		return Name;
	}
	
	public void setName(String Name) {
		this.Name = Name;
	}
	
	public String getCode() {
		return Code;
	}
	
	public void setCode(String Code) {
		this.Code = Code;
	}

}
